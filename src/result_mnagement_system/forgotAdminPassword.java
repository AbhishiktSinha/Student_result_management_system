    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        String username = usernameField.getText();
        String password = passwordField.getText();
        String fatherName = fatherNameField.getText();
        
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/rms", "root", "123abhi");

            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from admin_credentials where Username = '"+username+"'");
            if(rs.next())
            {
                if(rs.getString(3).equals(fatherName))
                {
                    st.executeUpdate("update admin_credentials set Password ='"+password+"' where Username = '"+username+"'");
                    JOptionPane.showMessageDialog(null, "Password changed succesfully");
                    setVisible(false);
                    new adminIndex().setVisible(true);
                }
                else
                    JOptionPane.showMessageDialog(null, "Check answer to security question");
            }
            else 
            {
                    JOptionPane.showMessageDialog(null, "Admin username doesn't exist");                
            }               
        }
        catch(Exception ee)
        {
            JOptionPane.showMessageDialog(null, ee.toString());
        }
    }//GEN-LAST:event_saveButtonActionPerformed

