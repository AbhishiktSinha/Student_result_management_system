public String getGrade(int marks)
{
    if(marks < 30) return "F";
    else if(marks >= 30 && marks < 50) return "D";
    else if(marks >= 50 && marks < 70) return "C";
    else if(marks >= 70 && marks < 80) return "B";
    else if(marks >= 80 && marks < 90) return "A";
    else return "A+";
}
