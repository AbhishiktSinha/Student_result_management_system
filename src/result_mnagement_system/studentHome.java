    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollNo = rollNoField.getText();
        System.out.println("Not started");
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/rms", "root", "123abhi");
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student inner join result where student.rollNo = "+rollNo+" and result.rollNo = '"+rollNo+"'");
            
            if(rs.next())
            {
                String result;
                String name = rs.getString(1);
                String dob = rs.getString(3);
                String gender = rs.getString(4);
                String course = rs.getString(5);
                String branch = rs.getString(6);
                
                System.out.print("Details found");
                String physMarks = rs.getString(8); int pm = Integer.parseInt(physMarks);
                String EMMarks = rs.getString(9); int emm = Integer.parseInt(EMMarks);
                String DBMSMarks = rs.getString(10); int dbmsm = Integer.parseInt(DBMSMarks);
                String OSMarks = rs.getString(11); int osm = Integer.parseInt(OSMarks);
                String OOPMarks = rs.getString(12); int oopm = Integer.parseInt(OOPMarks);
                
                int total_marksObt = pm + emm + dbmsm + osm + oopm;
                double perc = total_marksObt / 5.0;
                
                System.out.println("marks found");
                if(pm < 30 || dbmsm < 30 || emm < 30 || osm < 30 || oopm < 30)
                {
                    result = "FAIL";
                    resultField.setForeground(Color.RED);
                }
                else
                {
                    result = "PASS";
                    resultField.setForeground(Color.GREEN);
                }
                resultField.setText(result);               
                nameField.setText(name);
                courseField.setText(course);
                branchField.setText(branch);
                
                physField.setText(physMarks); physGradeField.setText(getGrade(pm));
                EMField.setText(EMMarks); EMGradeField.setText(getGrade(emm));
                DBMSField.setText(DBMSMarks); DBMSGradeField.setText(getGrade(dbmsm));
                OSField.setText(OSMarks); OSGradeField.setText(getGrade(osm));
                OOPField.setText(OOPMarks); OOPGradeField.setText(getGrade(oopm));
                finalGradeField.setText(getGrade((int)Math.ceil(perc)));
                
                totalMarksObtField.setText(String.valueOf(total_marksObt));
            }
            else
                JOptionPane.showMessageDialog(null, "Invalid Roll Number");
        }
        catch(Exception ee)
        {
            JOptionPane.showMessageDialog(null, ee.toString());
        }
    }//GEN-LAST:event_formComponentShown

