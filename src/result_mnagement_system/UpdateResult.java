    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollNumberField.getText();
        String subject = (String)subjectComboBox.getSelectedItem();
        String marks = marksField.getText();

        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            java.sql.Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/rms", "root", "123abhi");
            
            Statement st = con.createStatement();
            st.executeUpdate("update result set "+subject+" = "+marks+" where rollNo = "+rollNo);

            JOptionPane.showMessageDialog(null, "Marks updated succesfully");

            setVisible(false);
            new UpdateResult().setVisible(true);
        }
        catch(Exception ee)
        {
            JOptionPane.showMessageDialog(null, ee.toString());
        }

    }//GEN-LAST:event_saveButtonActionPerformed

