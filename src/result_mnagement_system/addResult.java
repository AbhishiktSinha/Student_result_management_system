    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:       
        String rollNo = rollNumberField.getText();
        String physics = physicsField.getText();
        String em = emField.getText();
        String dbms = dbmsField.getText();
        String os = osField.getText();
        String oop = oopField.getText();
        

        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/rms", "root", "123abhi");

            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollNo = '"+rollNo+"'");
            if(rs.next())
            {
                st.executeUpdate("insert into result(rollNo, Physics, EM, DBMS, OS, OOP) values('"+rollNo+"', '"+physics+"', '"+em+"', '"+dbms+"', '"+os+"', '"+oop+"')");
                JOptionPane.showMessageDialog(null, "Marks uploaded succesfully");
                setVisible(false);
                new addResult().setVisible(true);
            }
            else
                JOptionPane.showMessageDialog(null, "Unregistered roll number");   
            

        }
        catch(Exception ee)
        {
            JOptionPane.showMessageDialog(null, ee.toString());
        }

    }//GEN-LAST:event_saveButtonActionPerformed

