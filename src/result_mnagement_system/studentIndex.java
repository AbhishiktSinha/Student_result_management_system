    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // TODO add your handling code here:
        String rollNo = rollNoField.getText();
        
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/rms", "root", "123abhi");
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from result where result.rollNo = "+rollNo);
            
            if(rs.next())
            {
                setVisible(false);
                new studentHome(rollNo).setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Incorrect Roll Number or Result Not Added");
            }
            
        }
        catch(Exception ee)
        {
            JOptionPane.showMessageDialog(null, ee.toString());
        }
    }//GEN-LAST:event_loginButtonActionPerformed

